    private void EditbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditbtnMouseClicked

        if(UserIdTb.getText().isEmpty() || NameTb.getText().isEmpty() || PhoneTb.getText().isEmpty() || AddressTb.getText().isEmpty() || PasswordTb.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"missing Information");
        }
        else{
            try{
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bookshop","root", "Akku1805");
                String UId = UserIdTb.getText();
                String Query = "Update bookshop.user set Uname='"+NameTb.getText()+"',UPhone = '"+PhoneTb.getText()+"', UAddress='"+AddressTb.getText()+"', UPassword = '"+PasswordTb.getText()+"' where UID = "+UId ;
                Statement Delete = Con.createStatement();
                Delete.executeUpdate(Query);
                JOptionPane.showMessageDialog(this,"Books Updated");
                DisplayUsers();
                Reset();
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_EditbtnMouseClicked

