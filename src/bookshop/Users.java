    private void SaveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveBtnMouseClicked
        if(UserIdTb.getText().isEmpty() || NameTb.getText().isEmpty() || PhoneTb.getText().isEmpty() || AddressTb.getText().isEmpty() || PasswordTb.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"missing Information");
        }
        else{
            try{
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bookshop","root", "Akku1805");
                PreparedStatement add = Con.prepareStatement("insert into user values(?,?,?,?,?)");
                add.setInt(1,Integer.valueOf(UserIdTb.getText()));
                add.setString(2, NameTb.getText());
                add.setString(3, PhoneTb.getText());
                add.setString(4,AddressTb.getText());
                add.setString(5,PasswordTb.getText());
                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this,"User saved");
                DisplayUsers();
                Reset();
                //Con.close();
            }
            catch(Exception e){
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_SaveBtnMouseClicked

